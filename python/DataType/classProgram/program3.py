#Numeric DataType : int , float , complex

empId=10
empSal=6.4
eq=3+2j

print(empId)                        #10
print(empSal)                       #6.4
print(eq)                           #(3+2j)
print(type(empId))                  #<class 'int'>
print(type(empSal))                 #<class 'float'>
print(type(eq))                     #<class 'complex'>
